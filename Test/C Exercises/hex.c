#include <stdio.h>

int main()
{
    /* This function prints out the following integers in hex format */

    int int1;
    int int2;
    int int3;

    int1 = 15;
    int2 = 150;
    int3 = 1500;

    printf("int1 in hex format:%x\n", int1);
    printf("int2 in hex format:%x\n", int2);
    printf("int3 in hex format:%x\n", int3);

    return 0;
}