#include <stdio.h>

/* A program that converts a number to its character in a character set */

int main ()
{
    /* this function gives the corresponding characters of 72 and 104 */

    int digit_1 = 72;
    int digit_2 = 104;

    printf("The corresponding character of 72 is: %c\n", digit_1);
    printf("The corresponding character of 104 is: %c\n", digit_2);

    return 0;
}
